package com.vtalent.dropdown;

public class State {
    int id;
    int countryId;
    String name;
    

    public int getId() {
        return id;
    }
    public void setId(int id) {
		this.id = id;
	}


	public int getCountryId() {
		return countryId;
	}


	public void setCountryId(int countryId) {
		this.countryId = countryId;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	
}
